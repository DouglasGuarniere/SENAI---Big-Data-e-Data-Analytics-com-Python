# Captura o dado do usuario e guarda dentro da variavel chamada raio
import math


raio = float(input("Qual o raio do Circulo ? R:"))

# Calcula a area com a formula e armazena o resultado na variavel
area = 4*math.pi*math.sqrt**(raio)

# Mostra em tela o resultado
print(f"A area do Circulo {area}")